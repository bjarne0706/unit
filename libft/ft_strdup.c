#include "libft.h"

char		*ft_strdup(const char *src)
{
	char	*str;
	int		i;

	i = 0;
	str = (char*)malloc(ft_strlen(src) + 1);
	if (str == NULL)
		return (0);
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
