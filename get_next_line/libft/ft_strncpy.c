#include "includes/libft.h"

char		*ft_strncpy(char *s2, const char *s1, size_t num)
{
	size_t	i;

	i = 0;
	while (i < num && s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	while (i < num)
	{
		s2[i] = '\0';
		i++;
	}
	return (s2);
}
